package pessoas;

public class pessoa {
	protected String nome;
	protected int idade;
	protected double tamanho;
	protected String etinia;

	public pessoa(String nome, int idade, double tamanho, String etinia) {
		super();
		this.nome = nome;
		this.idade = idade;
		this.tamanho = tamanho;
		this.etinia = etinia;
	}

}
