package animais;

public class Animal {
	protected int pernas;
	protected String cabeca;
	protected String nome;
	protected int idade;

	public Animal(int pernas, String cabeca, String nome, int idade) {
		super();
		this.pernas = pernas;
		this.cabeca = cabeca;
		this.nome = nome;
		this.idade = idade;
	}

}
